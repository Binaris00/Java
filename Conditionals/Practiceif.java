package Conditionals;

public class Practiceif {
   public static void main(String[] args){
      String text = "";

      if (text == "Hello World"){
         System.out.println("Yes this is a hello word string!");
      }

      else if (text == ""){
         System.out.println("Yes this is an empty string!");
      }

      else{
         System.out.println("No this is not a hello word string! D:");
      }
   }
}
