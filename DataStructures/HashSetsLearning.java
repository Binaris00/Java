package DataStructures;

import java.util.HashSet;

public class HashSetsLearning {
   public static void main(String[] args) {
      HashSet<String> words = new HashSet<String>();
      
      words.add("Hi");
      words.add("Heloooooo");
      words.add("Please");
      words.add("GoodBye");

      System.out.println(words);
   }
}
