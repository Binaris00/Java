public class Casting {
   public static void main(String[] args) {
      

      // First example about casting (Converting data types to others)
      int number1 = 92;
      float number2 = 0.823f;

      // This convert automatically the number1 to a float value
      // Obviously you can use (float)number1 * number2
      System.out.println(number1 * number2);

      // Convert the number2 to a int value, so this remove the .023f
      System.out.println(number1 * (int)number2);


      
      







   }
}
